/**
 * Programmatic SEO landing pages ("use-case" pages).
 *
 * WHY: one landing page can't rank for every query — search engines rank a *page*
 * per intent, not a site. So each high-intent Russian query cluster gets its own
 * real page at /use-case/<slug> with a unique H1, copy, steps, benefits and FAQ.
 * This is the layer that makes "написал «создание шортсов» → нашлась наша аппка"
 * actually work.
 *
 * These pages are Russian-first (the founder's target queries are Russian, and the
 * Russian-speaking market is Yandex ≈ 73% / Google ≈ 26%). They are NOT thin doorway
 * pages: each carries substantive, unique content so Google/Yandex treat them as
 * useful, not spam. EN equivalents come with the later /ru i18n refactor — until then
 * we do NOT declare a fake hreflang alternate.
 *
 * To add a page: append an entry here. sitemap.ts, generateStaticParams and the
 * /use-case/[slug] route pick it up automatically. Keep copy honest and mapped to the
 * real product (explainable clips: hook + confidence score + reason; flash-free 9:16
 * reframe; credit pricing — see lib/plans.ts).
 *
 * Full semantic core / keyword research lives in docs/SEO_STRATEGY.md.
 */

export interface UseCaseStep {
  title: string;
  body: string;
}

export interface UseCaseBenefit {
  title: string;
  body: string;
}

export interface UseCaseFaqItem {
  q: string;
  a: string;
}

export interface UseCase {
  /** URL slug → /use-case/<slug>. Transliterated Russian (readable + indexable). */
  slug: string;
  /** Content language. Drives inLanguage / og:locale. Today: "ru" for all. */
  lang: "ru" | "en";
  /** Primary keyword this page targets (for our own reference / docs). */
  keyword: string;
  /** <title> — keep ≤ 60 chars where possible, keyword near the front. */
  title: string;
  /** <meta description> — ≤ ~155 chars, includes the keyword, sells the click. */
  metaDescription: string;
  /** Visible H1. */
  h1: string;
  /** Lead paragraph under the H1. */
  intro: string;
  /** "Как это работает" — 3 steps. */
  steps: UseCaseStep[];
  /** "Почему Quip" — 3 benefits specific to this use case. */
  benefits: UseCaseBenefit[];
  /** Unique FAQ → rendered + emitted as FAQPage JSON-LD. */
  faq: UseCaseFaqItem[];
  /** Closing CTA headline. */
  ctaTitle: string;
}

